function getInitials(name: string) {
  const parts = name.split(' ')
  const first = parts[0]
  const last = parts[parts.length - 1]
  if (first && last) {
    return `${first[0]}${last[0]}`.toUpperCase()
  }
  if (first) {
    return `${first[0]}`.toUpperCase()
  }
  return '??'
}

